//Write a function to find the area and perimeter of a Circle
let pi=3.14;
function perimeterOfCircle(r){
    return `Perimeter of Circle is ${2*pi*r}`
}

function areaOfCircle(rad){
    return `Area of circle is ${Math.PI*rad*rad}`
}

console.log(perimeterOfCircle(5))
console.log(perimeterOfCircle(10))
console.log(perimeterOfCircle(15))


console.log(areaOfCircle(5))
console.log(areaOfCircle(10))
console.log(areaOfCircle(15))
